const router = require("express").Router()

const fileUploader = require("../config/cloudinary.config")

// Profile picture
router.put(
    "/upload-picture",
    fileUploader.single("imageUrl"),
    (req, res, next) => {
        if (!req.file) {
            next(new Error("No file uploaded"))
            return
        }

        res.json({ secure_url: req.file.path })
    }
)

// Favicon
